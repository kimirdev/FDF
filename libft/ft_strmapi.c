#include "libft.h"

char				*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	unsigned char	*str;

	i = 0;
	if (!s)
		return (NULL);
	if (!(str = (unsigned char *)malloc(sizeof(unsigned char) *
										(ft_strlen(s) + 1))))
		return (NULL);
	while (s[i])
	{
		str[i] = (*f)(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return ((char *)str);
}
